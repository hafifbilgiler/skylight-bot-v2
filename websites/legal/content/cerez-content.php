<div class="section">
    <h2 class="section-title">3. Kullandığımız Çerezler</h2>
    <p><strong>Zorunlu Çerezler (Devre Dışı Bırakılamaz):</strong></p>
    <ul>
        <li><code>session_id</code> - Kullanıcı oturumu</li>
        <li><code>csrf_token</code> - Güvenlik tokeni</li>
        <li><code>auth_token</code> - Kimlik doğrulama</li>
    </ul>
    <p><strong>Tercih Çerezleri:</strong></p>
    <ul>
        <li><code>user_theme</code> - Tema tercihi</li>
        <li><code>user_language</code> - Dil tercihi</li>
    </ul>
    <p><strong>Analitik Çerezler:</strong></p>
    <ul>
        <li>Anonim kullanım istatistikleri</li>
        <li>Sayfa görüntüleme sayaçları</li>
    </ul>
</div>

<div class="section">
    <h2 class="section-title">4. Üçüncü Taraf Çerezleri</h2>
    <p>Bazı hizmetler için 3. taraf çerezler kullanılabilir:</p>
    <ul>
        <li><strong>Ödeme Sağlayıcıları:</strong> Stripe, PayPal (güvenli ödeme için)</li>
        <li><strong>CDN Hizmetleri:</strong> Cloudflare (hız optimizasyonu için)</li>
    </ul>
    <p>Bu çerezler kendi gizlilik politikalarına tabidir.</p>
</div>

<div class="section">
    <h2 class="section-title">5. Çerezleri Nasıl Kontrol Edersiniz?</h2>
    <p><strong>Tarayıcı Ayarları:</strong></p>
    <ul>
        <li>Chrome: Ayarlar → Gizlilik ve Güvenlik → Çerezler</li>
        <li>Firefox: Ayarlar → Gizlilik ve Güvenlik → Çerezler</li>
        <li>Safari: Tercihler → Gizlilik → Çerezler</li>
    </ul>
    <p><strong>Not:</strong> Zorunlu çerezleri devre dışı bırakırsanız platform düzgün çalışmayabilir.</p>
</div>

<div class="section">
    <h2 class="section-title">6. Çerezleri Silme</h2>
    <p>Tarayıcı ayarlarınızdan mevcut çerezleri silebilirsiniz. Bu işlem, oturum kapanmasına ve ayarlarınızın sıfırlanmasına neden olabilir.</p>
