    <h3>4.2. Güvenlik Önlemleri</h3>
    
    <h4>Teknik Güvenlik</h4>
    <ul>
        <li><strong>Şifreleme:</strong> Tüm veriler transit ve rest halinde AES-256 ile şifrelenir</li>
        <li><strong>HTTPS:</strong> SSL/TLS sertifikaları ile güvenli bağlantı</li>
        <li><strong>Güvenlik Duvarı:</strong> Gelişmiş firewall koruması</li>
        <li><strong>DDoS Koruması:</strong> Dağıtık saldırı önleme sistemleri</li>
        <li><strong>Güvenlik Güncellemeleri:</strong> Düzenli sistem yamaları</li>
    </ul>

    <h4>İdari Güvenlik</h4>
    <ul>
        <li>Sınırlı erişim: Sadece yetkili personel</li>
        <li>İki faktörlü kimlik doğrulama (2FA) zorunluluğu</li>
        <li>Düzenli güvenlik denetimleri</li>
        <li>Veri erişim logları tutulması</li>
        <li>Gizlilik sözleşmeleri (NDA)</li>
    </ul>

