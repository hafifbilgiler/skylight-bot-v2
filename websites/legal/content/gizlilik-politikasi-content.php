    <h2>8. Kullanıcı Hakları (KVKK Madde 11)</h2>
    
    <p>KVKK uyarınca aşağıdaki haklara sahipsiniz:</p>

    <h3>8.1. Bilgi Alma Hakkı</h3>
    <ul>
        <li>Kişisel verilerinizin işlenip işlenmediğini öğrenme</li>
        <li>İşlenmişse bunlar hakkında bilgi talep etme</li>
        <li>İşlenme amacını ve amacına uygun kullanılıp kullanılmadığını öğrenme</li>
    </ul>

    <h3>8.2. Düzeltme ve Silme Hakkı</h3>
    <ul>
        <li>Eksik veya yanlış işlenmiş verilerin düzeltilmesini talep etme</li>
        <li>Verilerin silinmesini veya yok edilmesini isteme</li>
        <li>Yapılan düzeltme/silme işleminin üçüncü taraflara bildirilmesini talep etme</li>
    </ul>

    <h3>8.3. İtiraz Hakkı</h3>
    <ul>
        <li>Otomatik sistemlerle verilerinizin işlenmesine itiraz etme</li>
        <li>Verilerinizin işlenmesi nedeniyle bir zarara uğramanız halinde giderilmesini talep etme</li>
    </ul>

