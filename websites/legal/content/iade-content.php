<div class="highlight-box">
    <h3><i class="fas fa-info-circle"></i> Hızlı İptal</h3>
    <p>Aboneliğinizi dilediğiniz zaman hesap ayarlarından iptal edebilirsiniz. İptal işlemi mevcut fatura döneminin sonunda geçerli olur.</p>
</div>

<div class="section">
    <h2 class="section-title">1. İptal Süreci</h2>
    <p><strong>Nasıl İptal Edilir:</strong></p>
    <ul>
        <li>Hesap → Ayarlar → Abonelik bölümüne gidin</li>
        <li>"Aboneliği İptal Et" butonuna tıklayın</li>
        <li>İptal nedeninizi belirtin (opsiyonel)</li>
        <li>Onaylayın</li>
    </ul>
    <p><strong>İptal Sonrası:</strong></p>
    <ul>
        <li>Mevcut fatura dönemi sonuna kadar hizmet devam eder</li>
        <li>Dönem bitiminde otomatik yenileme durdurulur</li>
        <li>Verileriniz 30 gün boyunca saklanır (yeniden aktive edilebilir)</li>
    </ul>
</div>

<div class="section">
    <h2 class="section-title">2. İade Koşulları</h2>
    <p><strong>14 Günlük İade Garantisi:</strong></p>
    <ul>
        <li>İlk kez abone olduysanız</li>
        <li>İlk 14 gün içinde iptal ederseniz</li>
        <li>Tam iade alırsınız</li>
    </ul>
    <p><strong>Kısmi İade:</strong></p>
    <ul>
        <li>Teknik arıza nedeniyle hizmet alamadıysanız</li>
        <li>Kullanılmayan gün sayısı hesaplanarak iade yapılır</li>
    </ul>
</div>

<div class="section">
    <h2 class="section-title">3. İade Başvuru Süreci</h2>
    <p>İade talebinde bulunmak için:</p>
    <ol style="margin:12px 0 12px 20px;color:var(--text-secondary);font-size:14px;">
        <li><a href="mailto:iade@skymerge.tech" style="color:var(--accent-cyan)">iade@skymerge.tech</a> adresine e-posta gönderin</li>
        <li>Konu başlığı: "İade Talebi - [Hesap E-postanız]"</li>
        <li>İade nedenini açıklayın</li>
        <li>7 iş günü içinde yanıt alırsınız</li>
    </ol>
</div>

<div class="section">
    <h2 class="section-title">4. İade Süresi</h2>
    <p>İade onaylandıktan sonra:</p>
    <ul>
        <li>7-14 iş günü içinde ödeme yönteminize iade yapılır</li>
        <li>Banka işlem süreleri değişiklik gösterebilir</li>
        <li>İade durumu e-posta ile bildirilir</li>
    </ul>
</div>

<div class="section">
    <h2 class="section-title">5. İade Yapılamayan Durumlar</h2>
    <ul>
        <li>14 günlük süre geçtiyse (normal iptal koşulları geçerlidir)</li>
        <li>Kullanıcı kaynaklı hesap askıya alınmasıysa</li>
        <li>Kural ihlali nedeniyle hesap kapatıldıysa</li>
    </ul>
</div>

<div class="section">
    <h2 class="section-title">6. Otomatik Yenileme İptali</h2>
    <p>Sadece otomatik yenilemeyi durdurup hizmeti kullanmaya devam edebilirsiniz:</p>
    <ul>
        <li>Hesap → Ayarlar → "Otomatik Yenilemeyi Durdur"</li>
        <li>Mevcut dönem sonunda abonelik biter</li>
        <li>İstediğiniz zaman yeniden başlatabilirsiniz</li>
    </ul>
</div>

